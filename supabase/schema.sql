create table if not exists public.game_runs (
  id bigint generated by default as identity primary key,
  run_id uuid,
  nickname text not null check (char_length(btrim(nickname)) between 1 and 12),
  play_mode text not null default 'pc' check (play_mode in ('pc', 'mobile')),
  stage1_ms bigint not null default 0 check (stage1_ms >= 0),
  stage2_ms bigint not null default 0 check (stage2_ms >= 0),
  stage3_ms bigint not null default 0 check (stage3_ms >= 0),
  total_ms bigint not null default 0 check (total_ms >= 0),
  completed boolean not null default true,
  completed_at timestamptz,
  created_at timestamptz not null default timezone('utc', now())
);

alter table public.game_runs add column if not exists run_id uuid;
alter table public.game_runs add column if not exists completed boolean not null default true;
alter table public.game_runs add column if not exists completed_at timestamptz;

create unique index if not exists game_runs_run_id_idx
  on public.game_runs (run_id)
  where run_id is not null;

create index if not exists game_runs_total_ms_idx
  on public.game_runs (total_ms asc, created_at asc);

alter table public.game_runs enable row level security;

drop policy if exists "Public leaderboard can be read" on public.game_runs;
create policy "Public leaderboard can be read"
  on public.game_runs for select
  to anon, authenticated
  using (true);

drop policy if exists "Players can submit a run" on public.game_runs;
create policy "Players can submit a run"
  on public.game_runs for insert
  to anon, authenticated
  with check (
    char_length(btrim(nickname)) between 1 and 12
    and play_mode in ('pc', 'mobile')
    and stage1_ms >= 0
    and stage2_ms >= 0
    and stage3_ms >= 0
    and total_ms >= 0
  );

drop policy if exists "Players can update a run" on public.game_runs;
create policy "Players can update a run"
  on public.game_runs for update
  to anon, authenticated
  using (true)
  with check (
    char_length(btrim(nickname)) between 1 and 12
    and play_mode in ('pc', 'mobile')
    and stage1_ms >= 0
    and stage2_ms >= 0
    and stage3_ms >= 0
    and total_ms >= 0
  );
